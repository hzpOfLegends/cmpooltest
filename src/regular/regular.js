// 邮箱正则
let mailReg = /[\w!#$%&'*+/=?^_`{|}~-]+(?:\.[\w!#$%&'*+/=?^_`{|}~-]+)*@(?:[\w](?:[\w-]*[\w])?\.)+[\w](?:[\w-]*[\w])?/;
// 密码正则
let pwdReg = /^(?=.*[a-zA-Z])(?=.*\d)(?=.*[~!@#$%^&*()_+`\-={}:";'<>?,.\/]).{8,24}$/;
// 手机正则
let telReg = /(^1[3|4|5|7|8]\d{9}$)|(^09\d{8}$)/;
export {mailReg,pwdReg,telReg}
